package Loan;

public class GoldLoan extends Loan {
	private  int Grams;
	private int purity;
	private long  Rate_intrest;
	private int tenure;
	private long balance;
	public int getGrams() {
		return Grams;
	}
	public void setGrams(int grams) {
		Grams = grams;
	}
	public int getPurity() {
		return purity;
	}
	public void setPurity(int purity) {
		this.purity = purity;
	}
	public long getRate_intrest() {
		return Rate_intrest;
	}
	public void setRate_intrest(long rate_intrest) {
		Rate_intrest = rate_intrest;
	}
	public int getTenure() {
		return tenure;
	}
	public void setTenure(int tenure) {
		this.tenure = tenure;
	}
	public long getBalance() {
		return balance;
	}
	public void setBalance(long balance) {
		this.balance = balance;
	}
	
	
}	
	



